function remove_holes_and_decrease(nbr, holes)
    # Remove holes
    nbr_filter = [filter(x -> x ∉ holes, sublist) for sublist in X_nbr]
    println("$(nbr_filter)")

    # Decrease remaining elements
    for i in 1:length(nbr_filter)
        for j in 1:length(nbr_filter[i])
            nbr_filter[i][j] -= count(h -> h < nbr_filter[i][j], holes)
        end
    end

    return nbr_filter
end

